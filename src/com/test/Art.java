package com.test;

public class Art {
	
	private String art_name , gallary_date, art_picture, email;
	
	private int art_id;

	public String getArt_name() {
		return art_name;
	}

	public void setArt_name(String art_name) {
		this.art_name = art_name;
	}

	public String getGallary_date() {
		return gallary_date;
	}

	public void setGallary_date(String gallary_date) {
		this.gallary_date = gallary_date;
	}

	public String getArt_picture() {
		return art_picture;
	}

	public void setArt_picture(String art_picture) {
		this.art_picture = art_picture;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getArt_id() {
		return art_id;
	}

	public void setArt_id(int art_id) {
		this.art_id = art_id;
	}

}
